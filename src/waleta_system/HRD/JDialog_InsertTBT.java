    private void button_choose_temanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_choose_temanActionPerformed
        // TODO add your handling code here:
        waleta_system.Browse_Karyawan dialog = new waleta_system.Browse_Karyawan(new javax.swing.JFrame(), true, null);
        dialog.pack();
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
        dialog.setEnabled(true);
        int x = waleta_system.Browse_Karyawan.table_list_karyawan.getSelectedRow();
        if (x != -1) {
            txt_id2.setText(waleta_system.Browse_Karyawan.table_list_karyawan.getValueAt(x, 0).toString());
            txt_nama2.setText(waleta_system.Browse_Karyawan.table_list_karyawan.getValueAt(x, 1).toString());
            txt_bagian2.setText(waleta_system.Browse_Karyawan.table_list_karyawan.getValueAt(x, 2).toString());
        }
        if (txt_bagian2.getText().length() < 5 || !(txt_bagian2.getText().contains("CABUT") || txt_bagian2.getText().contains("CETAK") || txt_bagian2.getText().contains("-OM-"))) {
            JOptionPane.showMessageDialog(this, "Maaf Teman yang dibawa harus masuk CABUT/CETAK/OM!");
            txt_id2.setText(null);
            txt_nama2.setText(null);
            txt_bagian2.setText(null);
        }
    }//GEN-LAST:event_button_choose_temanActionPerformed

    private void button_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_okActionPerformed
        if (txt_id1.getText() == null || txt_id2.getText() == null) {
            JOptionPane.showMessageDialog(this, "silahkan masukkan nama karyawan waleta dan temannya yang di bawa");
        } else if (txt_id1.getText().equals(txt_id2.getText())) {
            JOptionPane.showMessageDialog(this, "maaf anda memasukkan id yang sama");
        } else {
            try {
                // TODO add your handling code here:
                String Query = "INSERT INTO `tb_temanbawateman`(`id_teman`, `id_pegawai`) "
                        + "VALUES ('" + txt_id2.getText().toUpperCase() + "','" + txt_id1.getText() + "')";
                Utility.db.getConnection().createStatement();
                if ((Utility.db.getStatement().executeUpdate(Query)) == 1) {
                    this.dispose();
                    JOptionPane.showMessageDialog(this, "Data Inserted !");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
                Logger.getLogger(JDialog_InsertTBT.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_button_okActionPerformed

