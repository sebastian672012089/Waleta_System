    public JDialog_PindahGrup(java.awt.Frame parent, boolean modal, String id_pegawai) {
        super(parent, modal);
        initComponents();
        this.id = id_pegawai;
        try {
            query = "SELECT `tb_karyawan`.`nama_pegawai`, `tb_grup_cabut`.`kode_grup`, `tb_grup_cabut`.`kode_urut` "
                    + "FROM `tb_grup_cabut` JOIN `tb_karyawan` ON `tb_grup_cabut`.`id_pegawai` = `tb_karyawan`.`id_pegawai` "
                    + "WHERE `tb_grup_cabut`.`id_pegawai` = '" + id + "'";
            rs = Utility.db.getStatement().executeQuery(query);
            if (rs.next()) {
                label_id.setText(id);
                label_nama.setText(rs.getString("nama_pegawai"));
                label_grup_lama.setText(rs.getString("kode_grup"));
                label_no_lama.setText(rs.getString("kode_urut"));
            }

            query = "SELECT `kode_grup` FROM `tb_grup` ORDER BY `kode_grup`";
            rs = Utility.db.getStatement().executeQuery(query);
            while (rs.next()) {
                ComboBox_grup.addItem(rs.getString("kode_grup"));
            }
        } catch (Exception ex) {
            Logger.getLogger(JDialog_PindahGrup.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

