    private void button_pindahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_pindahActionPerformed
        try {
            Utility.db.getConnection().setAutoCommit(false);
            int no_urut_baru = 0;
            String sql = "SELECT MAX(`kode_urut`) AS 'MAX' FROM `tb_grup_cabut` WHERE `kode_grup` = '" + ComboBox_grup.getSelectedItem() + "' ";
            rs = Utility.db.getStatement().executeQuery(sql);
            if (rs.next()) {
                no_urut_baru = rs.getInt("MAX") + 1;
            }
//            String sql1 = "UPDATE `tb_grup_cabut` SET `kode_urut`=`kode_urut` + 1 "
//                    + "WHERE `kode_grup` = '" + ComboBox_grup.getSelectedItem() + "' AND `kode_urut` >= " + no_urut_baru + "";
//            Utility.db.getStatement().executeUpdate(sql1);
            String sql2 = "UPDATE `tb_grup_cabut` SET `kode_urut`=`kode_urut` - 1 "
                    + "WHERE `kode_grup` = '" + label_grup_lama.getText() + "' AND `kode_urut` > " + label_no_lama.getText();
            Utility.db.getStatement().executeUpdate(sql2);
            query = "UPDATE `tb_grup_cabut` SET `kode_grup`='" + ComboBox_grup.getSelectedItem() + "',`kode_urut`='" + no_urut_baru + "' "
                    + "WHERE `id_pegawai` = '" + id + "'";
            Utility.db.getStatement().executeUpdate(query);
            Utility.db.getConnection().commit();
            JOptionPane.showMessageDialog(this, "Pegawai berhasil di pindahkan ke grup " + ComboBox_grup.getSelectedItem());
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            try {
                Utility.db.getConnection().rollback();
            } catch (Exception e) {
                Logger.getLogger(JDialog_PindahGrup.class.getName()).log(Level.SEVERE, null, e);
            }
            Logger.getLogger(JDialog_PindahGrup.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                Utility.db.getConnection().setAutoCommit(true);
            } catch (SQLException ex) {
                Logger.getLogger(JDialog_PindahGrup.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_button_pindahActionPerformed

