    public boolean isGroupDuplicate(String kode_grup) {
        boolean result = false;
        try {
            sql = "SELECT * FROM `tb_grup` WHERE `kode_grup` = '" + kode_grup + "'";
            rs = Utility.db.getStatement().executeQuery(sql);
            result = rs.next();
        } catch (SQLException ex) {
            Logger.getLogger(JDialog_EditGrup.class.getName()).log(Level.SEVERE, null, ex);
        }
        return result;
    }

