    private void button_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_changeActionPerformed
        // TODO add your handling code here:
        String nama_baru = txt_namaGrup.getText();
        if (isGroupDuplicate(nama_baru) && !nama_baru.equals(nama_grup_lama)) {
            JOptionPane.showMessageDialog(this, "Maaf, nama Grup " +nama_baru+ " sudah ada");
        } else {
            try {
                sql = "UPDATE `tb_grup` SET `kode_grup`='"+nama_baru+"',`ruangan`='"+ComboBox_ruangan.getSelectedItem()+"' WHERE `kode_grup` = '"+nama_grup_lama+"'";
                if ((Utility.db.getStatement().executeUpdate(sql)) > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah !");
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(JDialog_EditGrup.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_button_changeActionPerformed


