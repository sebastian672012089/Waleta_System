    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        String tgl_selesai = "";
        if (Date_tgl_selesai.getDate() != null) {
            tgl_selesai = dateFormat.format(Date_tgl_selesai.getDate());
        }
        String Query = "UPDATE `tb_cabut` SET "
                + "`cabut_diterima`='" + txt_diterima.getText() + "',"
                + "`tgl_mulai_cabut`='" + dateFormat.format(Date_tgl_masuk.getDate()) + "',"
                + "`cabut_diserahkan`='" + txt_diserahkan.getText() + "',"
                + "`pekerja_hancuran`='" + txt_hancuran.getText() + "',"
                + "`pekerja_kopyok`='" + txt_kopyok.getText() + "',"
                + "`tgl_setor_cabut`='" + tgl_selesai + "',"
                + "`pecah_cabut`='" + txt_pecah.getText() + "',"
                + "`sobek_cabut`='" + txt_sobek.getText() + "',"
                + "`gumpil_cabut`='" + txt_gumpil.getText() + "',"
                + "`cabut_hilang_kaki`='" + txt_hilang_kaki.getText() + "',"
                + "`cabut_hilang_ujung`='" + txt_hilang_ujung.getText() + "',"
                + "`admin_cabut`='" + txt_admin.getText() + "', "
                + "`ketua_regu`='" + txt_ketua_regu.getText() + "' "
                + "WHERE `no_laporan_produksi`='" + label_no_lp.getText() + "'";
        try {
            Utility.db_sub.connect();
            Utility.db_sub.getConnection().createStatement();
            if ((Utility.db_sub.getStatement().executeUpdate(Query)) == 1) {
                JOptionPane.showMessageDialog(this, "sukses update data online");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "edit data gagal");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JDialog_Edit_Data_Cabut_sub.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_saveActionPerformed

