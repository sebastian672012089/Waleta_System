    public void getdata() {
        try {
            Utility.db_sub.connect();
            label_no_lp.setText(no_laporan_produksi);
            sql = "SELECT `no_laporan_produksi`, `pekerja_hancuran`, `pekerja_kopyok`, `cabut_diterima`, `tgl_mulai_cabut`, `cabut_diserahkan`, `tgl_setor_cabut`, `sobek_cabut`, `cabut_sobek_lepas`, `gumpil_cabut`, `pecah_cabut`, `cabut_pecah_2`, `cabut_lubang`, `cabut_hilang_kaki`, `cabut_hilang_ujung`, `cabut_kaki_besar`, `cabut_kaki_kecil`, `cabut_hilang_bawah`, `admin_cabut`, `ketua_regu`, `tgl_cabut` "
                    + "FROM `tb_cabut` WHERE `no_laporan_produksi` = '" + no_laporan_produksi + "'";
            rs = Utility.db_sub.getStatement().executeQuery(sql);
            if (rs.next()) {
                Date_tgl_masuk.setDate(rs.getDate("tgl_mulai_cabut"));
                txt_diterima.setText(rs.getString("cabut_diterima"));
                txt_hancuran.setText(rs.getString("pekerja_hancuran"));
                txt_kopyok.setText(rs.getString("pekerja_kopyok"));
                Date_tgl_selesai.setDate(rs.getDate("tgl_setor_cabut"));
                txt_diserahkan.setText(rs.getString("cabut_diserahkan"));
                txt_pecah.setText(rs.getString("pecah_cabut"));
                txt_sobek.setText(rs.getString("sobek_cabut"));
                txt_gumpil.setText(rs.getString("gumpil_cabut"));
                txt_hilang_kaki.setText(rs.getString("cabut_hilang_kaki"));
                txt_hilang_ujung.setText(rs.getString("cabut_hilang_ujung"));
                txt_admin.setText(rs.getString("admin_cabut"));
                txt_ketua_regu.setText(rs.getString("ketua_regu"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JDialog_Edit_Data_Cabut_sub.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

