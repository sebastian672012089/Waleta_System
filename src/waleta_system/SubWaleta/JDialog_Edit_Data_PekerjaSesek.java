    public void getdata() {
        try {
            Utility.db_sub.connect();
            sql = "SELECT `nomor`, `tb_detail_penyesek`.`no_lp_sesekan`, `tb_detail_penyesek`.`id_pegawai`, `tb_karyawan`.`nama_pegawai`, `tanggal_input`, `nilai_sesekan`, `gram_sesekan`, `tb_laporan_produksi_sesekan`.`sesekan_bersih`, `tb_laporan_produksi_sesekan`.`nilai_lp`  \n"
                    + "FROM `tb_detail_penyesek` "
                    + "LEFT JOIN `tb_karyawan` ON `tb_detail_penyesek`.`id_pegawai` = `tb_karyawan`.`id_pegawai`"
                    + "LEFT JOIN `tb_laporan_produksi_sesekan` ON `tb_laporan_produksi_sesekan`.`no_lp_sesekan` = `tb_detail_penyesek`.`no_lp_sesekan`\n"
                    + "WHERE `nomor` = '" + nomor + "'";
            rs = Utility.db_sub.getStatement().executeQuery(sql);
            if (rs.next()) {
                label_no_lp.setText(rs.getString("no_lp_sesekan"));
                txt_nomor.setText(nomor);
                Date_tgl.setDate(rs.getDate("tanggal_input"));
                txt_penyesek_id.setText(rs.getString("id_pegawai"));
                txt_penyesek_nama.setText(rs.getString("nama_pegawai"));
                txt_nilai_lp.setText(rs.getString("nilai_lp"));
                txt_sesekan_bersih.setText(rs.getString("sesekan_bersih"));
                txt_nilai_sesekan.setText(rs.getString("nilai_sesekan"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_Edit_Data_PekerjaSesek.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

