    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        try {
            boolean check = true;
            if (Date_tgl.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong");
                check = false;
            }
            if (check) {
                float nilai_lp = Float.valueOf(txt_nilai_lp.getText());
                float sesekan_bersih = Float.valueOf(txt_sesekan_bersih.getText());
                float nilai_sesekan = Float.valueOf(txt_nilai_sesekan.getText());
                float gram_dikerjakan = (nilai_sesekan / nilai_lp) * sesekan_bersih;
                gram_dikerjakan = Math.round(gram_dikerjakan * 10000f) / 10000f;
                String Query = "UPDATE `tb_detail_penyesek` SET "
                        + "`id_pegawai`='" + txt_penyesek_id.getText() + "',"
                        + "`tanggal_input`='" + dateFormat.format(Date_tgl.getDate()) + "',"
                        + "`nilai_sesekan`='" + nilai_sesekan + "', "
                        + "`gram_sesekan`='" + gram_dikerjakan + "' "
                        + "WHERE `nomor`='" + txt_nomor.getText() + "'";
                Utility.db_sub.connect();
                Utility.db_sub.getConnection().createStatement();
                if ((Utility.db_sub.getStatement().executeUpdate(Query)) == 1) {
                    JOptionPane.showMessageDialog(this, "sukses update data online");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "edit data gagal");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            Logger.getLogger(JDialog_Edit_Data_PekerjaSesek.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_saveActionPerformed

