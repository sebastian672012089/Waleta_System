    private void button_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_insertActionPerformed
        // TODO add your handling code here:
        try {
            String sql1 = "SELECT `jumlah_keping`, `berat_basah` FROM `tb_laporan_produksi` WHERE `no_laporan_produksi` = '" + txt_no_lp.getText() + "'";
            ResultSet rs1 = Utility.db.getStatement().executeQuery(sql1);
            int kpg = rs1.getInt("jumlah_keping");
            int gram = rs1.getInt("berat_basah");

            String sql2 = "SELECT `tgl_setor_f2` FROM `tb_finishing_2` WHERE `no_laporan_produksi` = '" + txt_no_lp.getText() + "'";
            ResultSet rs2 = Utility.db.getStatement().executeQuery(sql2);
            if (rs2.next()) {
                if (rs2.getDate("tgl_setor_f2") == null) {
                    if (CheckDuplicateLP(txt_no_lp.getText())) {
                        JOptionPane.showMessageDialog(this, "No LP " + txt_no_lp.getText() + " sudah Masuk di Tabel");
                    } else {
                        DefaultTableModel model = (DefaultTableModel) Tabel_data.getModel();
                        model.addRow(new Object[]{txt_no_lp.getText(), null, kpg, gram});
                        txt_no_lp.setText("");
                        txt_no_lp.requestFocus();
                        count();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "LP Sudah disetor dari F2 tidak bisa balen");
                }
            } else {
                JOptionPane.showMessageDialog(this, "LP belum Masuk di bagian F2");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_balen_f2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_insertActionPerformed

