    public void input_balen() {
        int total_baris = Tabel_data.getRowCount();
        Boolean Check = true;

        if (Tabel_data.getValueAt(total_baris - 1, 1) == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Input tidak boleh kosong");
            Check = false;
        }

        if (Check) {
            try {
                Utility.db.getConnection().setAutoCommit(false);
                for (int i = 0; i < total_baris; i++) {
                    String Query = "INSERT INTO `tb_balen_f2`(`no_laporan_produksi`, `tgl_balen`, `keping1`, `gram1`) "
                            + "VALUES ('" + Tabel_data.getValueAt(i, 0) + "','" + Tabel_data.getValueAt(i, 1) + "','" + Tabel_data.getValueAt(i, 2) + "','" + Tabel_data.getValueAt(i, 3) + "')";
                    Utility.db.getConnection().createStatement();
                    Utility.db.getStatement().executeUpdate(Query);
                }
                Utility.db.getConnection().commit();
                JOptionPane.showMessageDialog(this, "Input Data Sukses");
                this.dispose();
            } catch (SQLException e) {
                try {
                    Utility.db.getConnection().rollback();
                    JOptionPane.showMessageDialog(this, "Input Data Gagal");
                } catch (SQLException ex) {
                    Logger.getLogger(JDialog_balen_f2.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, e.getMessage());
                Logger.getLogger(JDialog_balen_f2.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                try {
                    Utility.db.getConnection().setAutoCommit(true);
                } catch (SQLException ex) {
                    Logger.getLogger(JDialog_balen_f2.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    public boolean CheckDuplicateLP(String no_box) {
        int i = Tabel_data.getRowCount();
        for (int j = 0; j < i; j++) {
            if (no_box.equals(Tabel_data.getValueAt(j, 0).toString())) {
                return true;
            }
        }
        return false;
    }

    public void count() {
        int a = Tabel_data.getRowCount();
        label_jumlahLP.setText(Integer.toString(a));
    }

