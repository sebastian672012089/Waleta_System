    private void button_delete_tr_selaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_delete_tr_selaActionPerformed
        // TODO add your handling code here:
        try {
            int j = Table_Data_tr_sela.getSelectedRow();
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data yang ingin di hapus !");
            } else {
                sql = "SELECT `no_laporan_produksi` FROM `tb_finishing_2` WHERE `tb_finishing_2`.`no_laporan_produksi` = '" + Table_Data_tr_sela.getValueAt(j, 0) + "'";
                rs = Utility.db.getStatement().executeQuery(sql);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Maaf tidak bisa hapus No LP ini karena sudah masuk F2, mohon hapus data F2 terlebih dahulu!");
                } else {
                    int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Warning", 0);
                    if (dialogResult == JOptionPane.YES_OPTION) {
                        // delete code here
                        String Query = "DELETE FROM `tb_cetak` WHERE `tb_cetak`.`no_laporan_produksi` = '" + Table_Data_tr_sela.getValueAt(j, 0) + "'";
                        Utility.db.getConnection().createStatement();
                        if ((Utility.db.getStatement().executeUpdate(Query)) == 1) {
                            JOptionPane.showMessageDialog(this, "data deleted!");
                            refreshTable_TR_Sela();
                        } else {
                            JOptionPane.showMessageDialog(this, "Delete Failed!");
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_delete_tr_selaActionPerformed

