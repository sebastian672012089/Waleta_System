    public void refreshTable_TR_Sela() {
        try {
            decimalFormat.setMaximumFractionDigits(2);
            float total_kpg = 0, total_gram = 0;
            DefaultTableModel model = (DefaultTableModel) Table_Data_tr_sela.getModel();
            model.setRowCount(0);
            String filter_tanggal = "";
            if (Date1_tr_sela.getDate() != null && Date2_tr_sela.getDate() != null) {
                filter_tanggal = "AND `tanggal_tr_sela` BETWEEN '" + dateFormat.format(Date1_tr_sela.getDate()) + "' and '" + dateFormat.format(Date2_tr_sela.getDate()) + "'";
            }
            sql = "SELECT `nomor`, `tb_treatment_sela`.`no_laporan_produksi`, `kode_grade`, `jumlah_keping`, `berat_basah`, `tb_treatment_sela`.`id_pegawai`, `tb_karyawan`.`nama_pegawai`, `waktu`, `tanggal_tr_sela` \n"
                    + "FROM `tb_treatment_sela` \n"
                    + "LEFT JOIN `tb_laporan_produksi` ON `tb_treatment_sela`.`no_laporan_produksi` = `tb_laporan_produksi`.`no_laporan_produksi`\n"
                    + "LEFT JOIN `tb_karyawan` ON `tb_treatment_sela`.`id_pegawai` = `tb_karyawan`.`id_pegawai`\n"
                    + "WHERE `tb_treatment_sela`.`no_laporan_produksi` LIKE '%" + txt_search_no_lp.getText() + "%'"
                    + filter_tanggal
                    + "ORDER BY `tanggal_tr_sela` DESC";
            rs = Utility.db.getStatement().executeQuery(sql);
            Object[] row = new Object[15];
            while (rs.next()) {
                row[0] = rs.getInt("nomor");
                row[1] = rs.getString("no_laporan_produksi");
                row[2] = rs.getString("kode_grade");
                row[3] = rs.getFloat("jumlah_keping");
                row[4] = rs.getFloat("berat_basah");
                row[5] = rs.getString("id_pegawai");
                row[6] = rs.getString("nama_pegawai");
                row[7] = rs.getTime("waktu");
                row[8] = rs.getDate("tanggal_tr_sela");
                model.addRow(row);
                total_kpg = total_kpg + rs.getFloat("jumlah_keping");
                total_gram = total_gram + rs.getFloat("berat_basah");
            }
            ColumnsAutoSizer.sizeColumnsToFit(Table_Data_tr_sela);
            int rowData = Table_Data_tr_sela.getRowCount();
            label_total_data_tr_sela.setText(Integer.toString(rowData));
            label_total_kpg_tr_sela.setText(decimalFormat.format(total_kpg));
            label_total_gram_tr_sela.setText(decimalFormat.format(total_gram));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JPanel_DataTRSela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

