    public void input_f2() {
        try {
            Boolean Check = true;
            if ("".equals(txt_no_lp.getText())
                    || Date_koreksi.getDate() == null
                    || "".equals(txt_id_pekerja.getText())
                    || "".equals(txt_nama_pekerja.getText())
                    || "".equals(txt_tanpa_kaki.getText())
                    || "".equals(txt_kaki_kecil.getText())
                    || "".equals(txt_kaki_besar.getText())
                    || "".equals(txt_flat.getText())
                    ) {
                JOptionPane.showMessageDialog(this, "anda belum melengkapi data");
                Check = false;
            }
            if (Check) {
                String Query = "UPDATE `tb_finishing_2` SET "
                        + "`tgl_f1`='" + dateFormat.format(Date_koreksi.getDate()) + "', "
                        + "`pekerja_f1`='" + txt_nama_pekerja.getText() + "', "
                        + "`tanpa_kaki_f1`='" + txt_tanpa_kaki.getText() + "', "
                        + "`kaki_kecil_f1`='" + txt_kaki_kecil.getText() + "', "
                        + "`kaki_besar_f1`='" + txt_kaki_besar.getText() + "', "
                        + "`flat_f1`='" + txt_flat.getText() + "' "
                        + "WHERE `no_laporan_produksi`='" + lp + "'";
                Utility.db.getConnection().createStatement();
                if ((Utility.db.getStatement().executeUpdate(Query)) == 1) {
                    this.dispose();
                    JOptionPane.showMessageDialog(this, "Data Inserted !");
                }
            }
        } catch (HeadlessException | SQLException e) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, e);
        }
    }

