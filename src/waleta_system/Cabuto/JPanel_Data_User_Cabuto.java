    private void button_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_hapusActionPerformed
        // TODO add your handling code here:
        try {
            int j = table_data_user.getSelectedRow();
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data yang ingin di hapus !");
            } else {
                int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Warning", 0);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    // delete code here
                    String Query = "DELETE FROM `tb_user` WHERE `id_user` = '" + table_data_user.getValueAt(j, 0).toString() + "'";
                    Utility.db_cabuto.connect();
                    Utility.db_cabuto.getConnection().createStatement();
                    if ((Utility.db_cabuto.getStatement().executeUpdate(Query)) == 1) {
                        refreshTable_user();
                        JOptionPane.showMessageDialog(this, "data deleted Successfully");
                    } else {
                        JOptionPane.showMessageDialog(this, "Delete FAILED");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JPanel_Data_User_Cabuto.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_hapusActionPerformed

