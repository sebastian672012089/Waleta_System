    public void refreshTable_user() {
        try {
            Utility.db_cabuto.connect();
            DefaultTableModel model = (DefaultTableModel) table_data_user.getModel();
            model.setRowCount(0);
            sql = "SELECT `id_user`, `username`, `password`, `nama_user`, `role`, `induk`, `saldo`, `nik_ktp`, `alamat`, `tanggal_lahir`, `no_hp`, `email`, `tanggal_masuk`, `tanggal_keluar`, `status`, `level` "
                    + "FROM `tb_user` "
                    + "WHERE "
                    + "`nama_user` LIKE '%" + txt_search.getText() + "%' "
                    + " ORDER BY `id_user`";
//            System.out.println(sql);
            rs = Utility.db_cabuto.getStatement().executeQuery(sql);
            Object[] row = new Object[20];
            while (rs.next()) {
                row[0] = rs.getString("id_user");
                row[1] = rs.getString("username");
                row[2] = rs.getString("password");
                row[3] = rs.getString("role");
                row[4] = rs.getString("induk");
                row[5] = rs.getString("nama_user");
                row[6] = rs.getString("nik_ktp");
                row[7] = rs.getString("alamat");
                row[8] = rs.getDate("tanggal_lahir");
                row[9] = rs.getString("no_hp");
                row[10] = rs.getString("email");
                row[11] = rs.getDate("tanggal_masuk");
                row[12] = rs.getDate("tanggal_keluar");
                row[13] = rs.getString("status");
                row[14] = rs.getInt("saldo");
                row[15] = rs.getString("level");
                model.addRow(row);
            }
            ColumnsAutoSizer.sizeColumnsToFit(table_data_user);
            int total_data = table_data_user.getRowCount();
            label_total_data.setText(decimalFormat.format(total_data));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JPanel_Data_User_Cabuto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

