    private void button_delete_customer_bakuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_delete_customer_bakuActionPerformed
        // TODO add your handling code here:
        try {
            int j = Table_grade_barang_jadi_cabuto.getSelectedRow();
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data yang ingin di hapus !");
            } else {
                int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Warning", 0);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    // delete code here
                    String Query = "DELETE FROM `tb_pricelist` WHERE `grade` = '" + Table_grade_barang_jadi_cabuto.getValueAt(j, 0).toString() + "'";
                    Utility.db_cabuto.getConnection().createStatement();
                    if ((Utility.db_cabuto.getStatement().executeUpdate(Query)) == 1) {
                        JOptionPane.showMessageDialog(this, "Hapus data berhasil!");
                        refresh_tabel_grade_bj_cabuto();
                        txt_grade_barang_jadi.setText(null);
                        txt_harga_evaluasi.setText(null);
                        txt_harga_jual.setText(null);
                    } else {
                        JOptionPane.showMessageDialog(this, "Hapus data gagal!");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JPanel_GradeBarangJadi_Cabuto.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_delete_customer_bakuActionPerformed

    private void button_update_customer_bakuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_update_customer_bakuActionPerformed
        // TODO add your handling code here:
        int j = Table_grade_barang_jadi_cabuto.getSelectedRow();
        try {
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih salah satu data pada tabel !");
            } else {
                String Query = "UPDATE `tb_pricelist` SET "
                        + "`grade` = '" + txt_grade_barang_jadi.getText() + "', "
                        + "`harga` = '" + txt_harga_evaluasi.getText() + "', "
                        + "`harga_jual` = '" + txt_harga_jual.getText() + "' "
                        + "WHERE `grade` = '" + Table_grade_barang_jadi_cabuto.getValueAt(j, 0).toString() + "'";
                Utility.db_cabuto.getStatement().executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Data Saved!!");
                refresh_tabel_grade_bj_cabuto();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JPanel_GradeBarangJadi_Cabuto.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_update_customer_bakuActionPerformed

