    private void refresh_tabel_grade_bj_cabuto() {
        try {
            Utility.db_cabuto.connect();
            DefaultTableModel model = (DefaultTableModel) Table_grade_barang_jadi_cabuto.getModel();
            model.setRowCount(0);
            String query = "SELECT `grade`, `harga`, `harga_jual` "
                    + "FROM `tb_pricelist` "
                    + "WHERE `grade` LIKE '%" + txt_search_grade.getText() + "%' \n";
            ResultSet result = Utility.db_cabuto.getStatement().executeQuery(query);
            Object[] row = new Object[15];
            while (result.next()) {
                row[0] = result.getString("grade");
                row[1] = result.getInt("harga");
                row[2] = result.getInt("harga_jual");
                model.addRow(row);
            }
            ColumnsAutoSizer.sizeColumnsToFit(Table_grade_barang_jadi_cabuto);
            label_total_data.setText(Integer.toString(Table_grade_barang_jadi_cabuto.getRowCount()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JPanel_GradeBarangJadi_Cabuto.class.getName()).log(Level.SEVERE, null, e);
        }
    }

