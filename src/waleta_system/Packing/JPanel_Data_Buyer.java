    private void button_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_insertActionPerformed
        // TODO add your handling code here:
        Boolean Check = true;
        try {
            if ("".equals(txt_kode_buyer.getText())
                    || "".equals(txt_nama_buyer.getText())) {
                JOptionPane.showMessageDialog(this, "Maaf, nama dan kode buyer tidak boleh kosong !");
                Check = false;
            }
            if (Check) {
                String Query = "INSERT INTO `tb_buyer`(`kode_buyer`, `nama`, `alamat`, `telp`, `fax`, `kategori`) "
                        + "VALUES ('" + txt_kode_buyer.getText() + "','" + txt_nama_buyer.getText() + "','" + txt_alamat_buyer.getText() + "','" + txt_no_telp_buyer.getText() + "','" + txt_no_fax_buyer.getText() + "','" + txt_kategori.getText() + "')";
                executeSQLQuery(Query, "inserted !");
                button_clear_text.doClick();
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_button_insertActionPerformed

    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        // TODO add your handling code here:
        int j = Table_buyer.getSelectedRow();
        Boolean Check = true;
        try {
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Please Select Row Data that you want to Update !");
            } else {
                if (Check) {
                    String Query = "UPDATE `tb_buyer` SET "
                            + "`kode_buyer`='" + txt_kode_buyer.getText() + "', "
                            + "`nama`='" + txt_nama_buyer.getText() + "',"
                            + "`alamat`='" + txt_alamat_buyer.getText() + "',"
                            + "`telp`='" + txt_no_telp_buyer.getText() + "',"
                            + "`fax`='" + txt_no_fax_buyer.getText() + "', "
                            + "`kategori`='" + txt_kategori.getText() + "' "
                            + "WHERE `kode_buyer`='" + Table_buyer.getValueAt(j, 0) + "'";
                    executeSQLQuery(Query, "updated !");
                    button_clear_text.doClick();
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_button_updateActionPerformed

