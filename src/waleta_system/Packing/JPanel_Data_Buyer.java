    public void refreshTable() {
        
        DefaultTableModel model = (DefaultTableModel) Table_buyer.getModel();
        model.setRowCount(0);
        try {
            sql = "SELECT * FROM `tb_buyer` WHERE `nama` LIKE '%" + txt_search_nama_buyer.getText() + "%'";
            rs = Utility.db.getStatement().executeQuery(sql);
            Object[] row = new Object[6];
            while (rs.next()) {
                row[0] = rs.getString("kode_buyer");
                row[1] = rs.getString("nama");
                row[2] = rs.getString("alamat");
                row[3] = rs.getString("telp");
                row[4] = rs.getString("fax");
                row[5] = rs.getString("kategori");
                model.addRow(row);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JPanel_Data_Buyer.class.getName()).log(Level.SEVERE, null, ex);
        }
        label_total_data.setText(Integer.toString(Table_buyer.getRowCount()));
        ColumnsAutoSizer.sizeColumnsToFit(Table_buyer);
    }

    public void executeSQLQuery(String query, String message) {
        Utility.db.getConnection();
        try {
            Utility.db.getConnection().createStatement();
            if ((Utility.db.getStatement().executeUpdate(query)) == 1) {
                JOptionPane.showMessageDialog(this, "data " + message + " Successfully");
                refreshTable();
            } else {
                JOptionPane.showMessageDialog(this, "data not " + message);
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JPanel_Data_Buyer.class.getName()).log(Level.SEVERE, null, e);
        }
    }

