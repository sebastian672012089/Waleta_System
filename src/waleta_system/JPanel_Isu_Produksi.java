    public void refreshTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) tabel_data_isu_produksi.getModel();
            model.setRowCount(0);
            String filter_tanggal = "";
            if (Date_Search1.getDate() != null && Date_Search2.getDate() != null) {
                filter_tanggal = " AND `tanggal_isu` BETWEEN '" + dateFormat.format(Date_Search1.getDate()) + "' AND '" + dateFormat.format(Date_Search2.getDate()) + "'";
            }
            sql2 = "SELECT `kode_isu`, `masalah`, `dept`, `penanggungjawab`, `tanggal_isu`, `penyelesaian`, `gambar_isu`, `tanggal_selesai` "
                    + "FROM `tb_isu_produksi` WHERE `masalah` LIKE '%" + txt_search_keywords.getText() + "%'"
                    + filter_tanggal + " ORDER BY `tanggal_selesai`, `tanggal_isu` DESC";

            rs = Utility.db.getStatement().executeQuery(sql2);
            Object[] row = new Object[7];
            while (rs.next()) {
                row[0] = rs.getString("kode_isu");
                row[1] = rs.getDate("tanggal_isu");
                row[2] = rs.getString("masalah");
                row[3] = rs.getString("dept");
                row[4] = rs.getString("penanggungjawab");
                row[5] = rs.getString("penyelesaian");
                row[6] = rs.getDate("tanggal_selesai");
                model.addRow(row);
            }
            label_total_data.setText(Integer.toString(tabel_data_isu_produksi.getRowCount()));
            ColumnsAutoSizer.sizeColumnsToFit(tabel_data_isu_produksi);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            Logger.getLogger(JPanel_Isu_Produksi.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    public void showImage(String kode) {
        try {
            sql = "SELECT `kode_isu`, `gambar_isu` FROM `tb_isu_produksi` WHERE `kode_isu` = '" + kode + "'";
            rs = Utility.db.getStatement().executeQuery(sql);
            if (rs.next()) {
                label_image.setIcon(Utility.ResizeImage(null, rs.getBytes("gambar_isu"), label_image.getWidth(), label_image.getHeight()));
                label_image1.setIcon(Utility.ResizeImage("\\\\192.168.10.2\\Shared Folder\\DOKUMEN SISTEM\\ISSUE_IMAGE\\" + rs.getString("kode_isu") + ".JPG", null, label_image1.getWidth(), label_image1.getHeight()));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            Logger.getLogger(JPanel_Isu_Produksi.class.getName()).log(Level.SEVERE, null, e);
        }
    }

