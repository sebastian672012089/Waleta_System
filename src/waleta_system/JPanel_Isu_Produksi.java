    private void button_delete_isuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_delete_isuActionPerformed
        // TODO add your handling code here:
        try {
            int j = tabel_data_isu_produksi.getSelectedRow();
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data yang mau di hapus !");
            } else {
                int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Warning", 0);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    // delete code here
                    String Query = "DELETE FROM `tb_isu_produksi` WHERE `kode_isu` = '" + tabel_data_isu_produksi.getValueAt(j, 0).toString() + "'";
                    Utility.db.getConnection().createStatement();
                    if ((Utility.db.getStatement().executeUpdate(Query)) > 0) {
                        JOptionPane.showMessageDialog(this, "Data terhapus !");
                        refreshTable();
                    }
                }
            }
        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JPanel_Isu_Produksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_delete_isuActionPerformed

