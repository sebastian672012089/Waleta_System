    private void btnMailBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMailBrowseActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter docx = new FileNameExtensionFilter("MS Word file(.docx)", "docx");
        FileNameExtensionFilter doc = new FileNameExtensionFilter("MS Word file(.doc)", "doc");
        FileNameExtensionFilter pdf = new FileNameExtensionFilter("Pdf file(.pdf)", "pdf");
        chooser.setMultiSelectionEnabled(true);
        chooser.addChoosableFileFilter(docx);
        chooser.addChoosableFileFilter(doc);
        chooser.addChoosableFileFilter(pdf);
        chooser.setFileFilter(docx);
        chooser.setAcceptAllFileFilterUsed(false);
        chooser.showOpenDialog(this);
        File[] selectedFile = chooser.getSelectedFiles();
        mailSelectedFiles = new String[selectedFile.length];
        int i = 0;
        String pathText = "";
        for (File file : selectedFile) {
            mailSelectedFiles[i] = file.getAbsolutePath();
            pathText += "\"" + mailSelectedFiles[i] + "\"; ";
            i++;
        }
        txtMailAttachmentPath.setText(pathText);
    }//GEN-LAST:event_btnMailBrowseActionPerformed

