    public void checkJToggleButtonPerkecil(boolean checked) {
        if (checked) {
            jToggleButtonPerkecil.setText("ðŸ—–");
            jPanelAlat.setVisible(false);
            jButtonFoto2.setVisible(true);
            jButtonUbah2.setVisible(true);
            jButtonHapus2.setVisible(true);
            jButtonFoto.setVisible(false);
            jButtonUbah.setVisible(false);
            jButtonHapus.setVisible(false);
        } else {
            jToggleButtonPerkecil.setText("ðŸ—•");
            jPanelAlat.setVisible(true);
            jButtonFoto2.setVisible(false);
            jButtonUbah2.setVisible(false);
            jButtonHapus2.setVisible(false);
            jButtonFoto.setVisible(true);
            jButtonUbah.setVisible(true);
            jButtonHapus.setVisible(true);
        }
    }

