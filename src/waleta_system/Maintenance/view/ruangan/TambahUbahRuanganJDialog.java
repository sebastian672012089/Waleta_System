    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        switch (kategoriDialog) {
            case TAMBAH:
                try {
                    Ruangan newRuangan = new Ruangan(jTextFieldNamaRuangan.getText(), new Lantai(lantais.get(jComboBoxLantai.getSelectedIndex()).getIdLantai()));
                    if(new RuanganDAO().insert(mainJFrame, newRuangan))
                    {
                        JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Sukses menyimpan ruangan", "Sukses Simpan", JOptionPane.INFORMATION_MESSAGE);
                        mainJFrame.refreshRuangan();
                        dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal menyimpan ruangan", "Sukses Simpan", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal menyimpan ruangan: "+e.getLocalizedMessage(), "Gagal Simpan", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case UBAH:
                try {
                    Ruangan newRuangan = new Ruangan(jTextFieldNamaRuangan.getText(), new Lantai(lantais.get(jComboBoxLantai.getSelectedIndex()).getIdLantai()));
                    if(new RuanganDAO().update(mainJFrame, idRuanganUpdate, newRuangan))
                    {
                        JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Sukses menyimpan ruangan", "Sukses Simpan", JOptionPane.INFORMATION_MESSAGE);
                        mainJFrame.refreshRuangan();
                        dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal menyimpan ruangan", "Sukses Simpan", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal menyimpan ruangan: "+e.getLocalizedMessage(), "Gagal Simpan", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed


