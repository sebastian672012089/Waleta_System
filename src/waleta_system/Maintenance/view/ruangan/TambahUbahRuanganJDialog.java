    public TambahUbahRuanganJDialog(java.awt.Frame parent, boolean modal, MainFrame_Maintenance mainJFrame, KategoriDialog kategoriDialog, Ruangan ruangan) {
        this(parent, modal, mainJFrame, kategoriDialog);
        idRuanganUpdate = ruangan.getIdRuangan();
        jTextFieldNamaRuangan.setText(ruangan.getNamaRuangan());
        for (int i = 0; i < lantais.size(); i++) {
            if(lantais.get(i).getIdLantai() == ruangan.getLantai().getIdLantai())
            {
                jComboBoxLantai.setSelectedIndex(i);
                break;
            }
        }
    }

    public TambahUbahRuanganJDialog(java.awt.Frame parent, boolean modal, MainFrame_Maintenance mainJFrame, KategoriDialog kategoriDialog) {
        super(parent, modal);
        initComponents();
        try {
            lantais = new LantaiDAO().selectAll();
            if(lantais.size()>0)
            {
                String[] dataLantaiString = new String[lantais.size()];
                for (int i = 0; i < lantais.size(); i++) {
                    dataLantaiString[i] = lantais.get(i).getNamaLantai();
                }
                jComboBoxLantai.setModel(new javax.swing.DefaultComboBoxModel<>(dataLantaiString));
                this.mainJFrame = mainJFrame;
                this.kategoriDialog = kategoriDialog;
                switch (kategoriDialog) {
                    case TAMBAH:
                        setTitle("Waleta Maintenance - Waleta Maintenance - Tambah Ruangan");
                        break;
                    case UBAH:
                        setTitle("Waleta Maintenance - Waleta Maintenance - Ubah Ruangan");
                        break;
                }
            }
            else
            {
                JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal mengambil data Lantai: Pastikan terdapat setidaknya satu buah data lantai", "Gagal", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(TambahUbahRuanganJDialog.this, "Gagal mengambil data Lantai: " + ex.getLocalizedMessage(), "Gagal", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }

