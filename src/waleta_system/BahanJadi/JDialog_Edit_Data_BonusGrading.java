    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        Utility.db.getConnection();
        try {
            boolean check = true;

            if (check) {
                sql = "UPDATE `tb_bonus_grading` SET "
                        + "`jumbo`=" + txt_jumbo.getText() + ", "
                        + "`jumbo_r`=" + txt_jumbo_r.getText() + ", "
                        + "`super`=" + txt_super.getText() + ", "
                        + "`super_r`=" + txt_super_r.getText() + ", "
                        + "`premium`=" + txt_premium.getText() + ", "
                        + "`premium_r`=" + txt_premium_r.getText() + ", "
                        + "`standart`=" + txt_standart.getText() + ", "
                        + "`oval_1`=" + txt_oval_1.getText() + ", "
                        + "`oval_2`=" + txt_oval_2.getText() + ", "
                        + "`segitiga_besar`=" + txt_segitiga_besar.getText() + ", "
                        + "`segitiga_1`=" + txt_segitiga_1.getText() + ", "
                        + "`segitiga_2`=" + txt_segitiga_2.getText() + ", "
                        + "`pch_flat`='" + txt_pch_flat.getText() + "', "
                        + "`last_update` = NOW() "
                        + "WHERE `grade_baku`='" + label_grade_baku.getText() + "'";
                Utility.db.getStatement().executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Data " + label_grade_baku.getText() + " berhasil diubah");
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Logger.getLogger(JDialog_Edit_Data_BonusGrading.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_button_saveActionPerformed

