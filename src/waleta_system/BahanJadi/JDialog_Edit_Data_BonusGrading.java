                txt_oval_2.setText(rs.getString("oval_2"));
                txt_segitiga_besar.setText(rs.getString("segitiga_besar"));
                txt_segitiga_1.setText(rs.getString("segitiga_1"));
                txt_segitiga_2.setText(rs.getString("segitiga_2"));
                txt_pch_flat.setText(rs.getString("pch_flat"));
