    public JDialog_Aset_DataMasterAset_InsertEdit(java.awt.Frame parent, boolean modal, String kode) {
        super(parent, modal);
        initComponents();
        this.setResizable(false);
        try {
            if (kode == null) {
                label_title.setText("Tambah Data Aset");
                this.operand = "tambah";
                txt_kode_aset.setText(newKode());
            } else {
                label_title.setText("Edit Data Aset");
                this.operand = "edit";
                txt_kode_aset.setText(kode);

                sql = "SELECT `kode_aset`, `nama_aset`, `merk_aset`, `keterangan_aset`, `kategori_aset`, `depresiasi_aset` "
                        + "FROM `tb_aset_master` WHERE `kode_aset` = '" + kode + "'";
                rs = Utility.db.getStatement().executeQuery(sql);
                if (rs.next()) {
                    txt_kode_aset.setText(rs.getString("kode_aset"));
                    txt_nama_aset.setText(rs.getString("nama_aset"));
                    txt_merk.setText(rs.getString("merk_aset"));
                    txt_keterangan.setText(rs.getString("keterangan_aset"));
                    txt_kategori.setText(rs.getString("kategori_aset"));
                    Spinner_depresiasi.setValue(rs.getInt("depresiasi_aset"));
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_Aset_DataMasterAset_InsertEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public String newKode() {
        try {
            String kode_baru = "";
            sql = "SELECT MAX(`kode_aset`) AS 'kode_terakhir' FROM `tb_aset_master` WHERE 1";
            rs = Utility.db.getStatement().executeQuery(sql);
            if (rs.next()) {
                kode_baru = String.format("%04d", rs.getInt("kode_terakhir") + 1);
            }
            return kode_baru;
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_Aset_DataMasterAset_InsertEdit.class.getName()).log(Level.SEVERE, null, ex);
            return "";
        }
    }

    public boolean NoDuplicateAsset(String nama, String merk) {
        try {
            sql = "SELECT * FROM `tb_aset_master` WHERE `nama_aset` = '" + nama + "' AND `merk_aset` = '" + merk + "'";
            rs = Utility.db.getStatement().executeQuery(sql);
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Nama dan Merk tsb sudah ada\n"
                        + "kode:" + rs.getString("kode_aset") + "\n"
                        + "nama:" + rs.getString("nama_aset") + "\n"
                        + "merk:" + rs.getString("merk_aset") + "");
                return false;
            } else {
                return true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_Aset_DataMasterAset_InsertEdit.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

