    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        if (txt_nama_aset.getText() == null || txt_nama_aset.getText().equals("") ||
                txt_merk.getText() == null || txt_merk.getText().equals("")
                ) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data diatas, nama dan merk harus terisi");
        } else if (NoDuplicateAsset(txt_nama_aset.getText(), txt_merk.getText())) {
            if (operand.equals("tambah")) {
                try {
                    sql = "INSERT INTO `tb_aset_master`(`kode_aset`, `nama_aset`, `merk_aset`, `keterangan_aset`, `kategori_aset`, `depresiasi_aset`)"
                            + "VALUES ('" + txt_kode_aset.getText() + "','" + txt_nama_aset.getText() + "','" + txt_merk.getText() + "','" + txt_keterangan.getText() + "','" + txt_kategori.getText() + "','" + Spinner_depresiasi.getValue() + "')";
                    Utility.db.getConnection().createStatement();
                    if ((Utility.db.getStatement().executeUpdate(sql)) == 1) {
                        this.dispose();
                        JOptionPane.showMessageDialog(this, "Data Berhasil simpan");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                    Logger.getLogger(JDialog_Aset_DataMasterAset_InsertEdit.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (operand.equals("edit")) {
                try {
                    sql = "UPDATE `tb_aset_master` SET "
                            + "`nama_aset`='" + txt_nama_aset.getText() + "',"
                            + "`merk_aset`='" + txt_merk.getText() + "',"
                            + "`keterangan_aset`='" + txt_keterangan.getText() + "',"
                            + "`kategori_aset`='" + txt_kategori.getText() + "',"
                            + "`depresiasi_aset`=" + Spinner_depresiasi.getValue() + " "
                            + "WHERE `kode_aset`='" + txt_kode_aset.getText() + "'";
                    Utility.db.getConnection().createStatement();
                    if ((Utility.db.getStatement().executeUpdate(sql)) == 1) {
                        this.dispose();
                        JOptionPane.showMessageDialog(this, "Data Berhasil simpan");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                    Logger.getLogger(JDialog_Aset_DataMasterAset_InsertEdit.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_button_saveActionPerformed


