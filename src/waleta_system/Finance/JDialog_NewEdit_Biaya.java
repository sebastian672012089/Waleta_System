    public JDialog_NewEdit_Biaya(java.awt.Frame parent, boolean modal, String operand, String kode) {
        super(parent, modal);
        initComponents();
        this.setResizable(false);
        this.operand = operand;
        this.kode = kode;
        try {
            
            if (operand.equals("new")) {
                label_title.setText("Tambah Data");
                label_no.setText("");
            } else if (operand.equals("edit")) {
                label_title.setText("Edit Data");
                label_no.setText(kode);

                sql = "SELECT `bulan`, `Kategori1`, `jenis_pengeluaran`, `nilai` "
                        + "FROM `tb_biaya_pabrik` WHERE `no` = '" + kode + "'";
                rs = Utility.db.getStatement().executeQuery(sql);
                Date date = null;
                if (rs.next()) {
                    date = rs.getDate("bulan");
                    Calendar cal = Calendar.getInstance();
                    cal.setTime(date);
                    int month = cal.get(Calendar.MONTH);
                    int year = cal.get(Calendar.YEAR);
                    MonthChooser.setMonth(month);
                    YearChooser.setYear(year);
                    txt_kategori.setText(rs.getString("Kategori1"));
                    txt_jenis_biaya.setText(rs.getString("jenis_pengeluaran"));
                    txt_jumlah.setText(rs.getString("nilai"));
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(JDialog_InputBTK.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

