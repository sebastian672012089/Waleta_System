    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        if (txt_kategori.getText() == null || txt_jenis_biaya.getText() == null || txt_jumlah.getText() == null
                || txt_kategori.getText().equals("") || txt_jenis_biaya.getText().equals("") || txt_jumlah.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data diatas");
        } else {
            Calendar calendar = Calendar.getInstance();
            calendar.set(YearChooser.getYear(), MonthChooser.getMonth(), 01);
            Date date_input = calendar.getTime();
            int jumlah = Integer.valueOf(txt_jumlah.getText());
            if (operand.equals("new")) {
                try {
                    sql = "INSERT INTO `tb_biaya_pabrik`(`bulan`, `Kategori1`, `jenis_pengeluaran`, `nilai`) "
                            + "VALUES ('" + new SimpleDateFormat("yyyy-MM-dd").format(date_input) + "','" + txt_kategori.getText() + "','" + txt_jenis_biaya.getText() + "'," + jumlah + ")";
                    Utility.db.getConnection().createStatement();
                    if ((Utility.db.getStatement().executeUpdate(sql)) == 1) {
                        this.dispose();
                        JOptionPane.showMessageDialog(this, "Data Berhasil simpan");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                    Logger.getLogger(JDialog_NewEdit_Biaya.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Maaf format angka salah");
                    Logger.getLogger(JDialog_NewEdit_Biaya.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (operand.equals("edit")) {
                try {
                    sql = "UPDATE `tb_biaya_pabrik` SET "
                            + "`bulan`='" + new SimpleDateFormat("yyyy-MM-dd").format(date_input) + "',"
                            + "`Kategori1`='" + txt_kategori.getText() + "',"
                            + "`jenis_pengeluaran`='" + txt_jenis_biaya.getText() + "',"
                            + "`nilai`=" + jumlah + " "
                            + "WHERE `no`='" + kode + "'";
                    Utility.db.getConnection().createStatement();
                    if ((Utility.db.getStatement().executeUpdate(sql)) == 1) {
                        this.dispose();
                        JOptionPane.showMessageDialog(this, "Data Berhasil simpan");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                    Logger.getLogger(JDialog_NewEdit_Biaya.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Maaf format angka salah");
                    Logger.getLogger(JDialog_NewEdit_Biaya.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_button_saveActionPerformed


