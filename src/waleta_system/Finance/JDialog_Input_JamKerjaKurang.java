    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        try {
            int count = 0;
            for (int i = 0; i < tabel_data_jam_kerja_kurang.getRowCount(); i++) {
                String tanggal = tabel_data_jam_kerja_kurang.getValueAt(i, 4).toString();
                String id_pegawai = tabel_data_jam_kerja_kurang.getValueAt(i, 0).toString();
                String Query = "INSERT INTO `tb_cuti`(`id_pegawai`, `tanggal_cuti`, `jenis_cuti`, `kategori_cuti`, `keterangan`) "
                        + "SELECT * FROM (SELECT '" + id_pegawai + "','" + tanggal + "', 'Absen', 'Tanpa Keterangan','-' AS 'keterangan') AS tmp\n"
                        + "WHERE NOT EXISTS (SELECT `kode_cuti` FROM `tb_cuti` WHERE `id_pegawai` = '" + id_pegawai + "' AND `tanggal_cuti` = '" + tanggal + "')";
                Utility.db.getConnection().createStatement();
                if (Utility.db.getStatement().executeUpdate(Query) == 1) {
                    count++;
                }
            }
            JOptionPane.showMessageDialog(this, count + " data berhasil di input ke data cuti/absen !");
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JDialog_Input_JamKerjaKurang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_saveActionPerformed

