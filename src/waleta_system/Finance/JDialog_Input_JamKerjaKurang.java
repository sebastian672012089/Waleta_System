    public void refreshTable(Date tgl_mulai, Date tgl_selesai) {
        try {
            String tanggal_mulai = dateFormat.format(tgl_mulai);
            String tanggal_selesai = dateFormat.format(tgl_selesai);

            DefaultTableModel model = (DefaultTableModel) tabel_data_jam_kerja_kurang.getModel();
            model.setRowCount(0);
            sql = "SELECT `tb_lembur_rekap`.`id_pegawai`, `tb_karyawan`.`nama_pegawai`, CONCAT(`divisi_bagian`, '-', `ruang_bagian`) AS 'nama_bagian',\n"
                    + "DAYNAME(`tanggal`) AS 'hari', `tanggal`, `premi_hadir` \n"
                    + "FROM `tb_lembur_rekap` \n"
                    + "LEFT JOIN `tb_karyawan` ON `tb_lembur_rekap`.`id_pegawai` = `tb_karyawan`.`id_pegawai`\n"
                    + "LEFT JOIN `tb_bagian` ON `tb_karyawan`.`kode_bagian` = `tb_bagian`.`kode_bagian`\n"
                    + "WHERE `premi_hadir` = 0\n"
                    + "AND `tanggal` BETWEEN '" + tanggal_mulai + "' AND '" + tanggal_selesai + "'";
            rs = Utility.db.getStatement().executeQuery(sql);
            Object[] row = new Object[10];
            while (rs.next()) {
                row[0] = rs.getString("id_pegawai");
                row[1] = rs.getString("nama_pegawai");
                row[2] = rs.getString("nama_bagian");
                row[3] = rs.getString("hari");
                row[4] = rs.getDate("tanggal");
                model.addRow(row);
            }
            ColumnsAutoSizer.sizeColumnsToFit(tabel_data_jam_kerja_kurang);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            Logger.getLogger(JDialog_Input_JamKerjaKurang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

