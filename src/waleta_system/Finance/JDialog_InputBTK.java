                    + "`packing`="+Integer.valueOf(txt_packing.getText())+" "
                    + "WHERE 1";
            System.out.println(sql);
            if ((Utility.db.getStatement().executeUpdate(sql)) == 1) {
                this.dispose(); 
            }
