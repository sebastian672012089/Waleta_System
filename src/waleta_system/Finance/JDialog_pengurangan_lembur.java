    public void refresh_tabel() {
        try {
            
            DefaultTableModel model = (DefaultTableModel) Table_data_pengurangan_lembur.getModel();
            model.setRowCount(0);
            sql = "SELECT `tgl_lembur`, `tb_lembur_pengurangan`.`id_pegawai`, `tb_karyawan`.`nama_pegawai`, `tb_bagian`.`nama_bagian`, `pengurangan`, `tb_lembur_pengurangan`.`keterangan` \n"
                    + "FROM `tb_lembur_pengurangan` \n"
                    + "LEFT JOIN `tb_karyawan` ON `tb_lembur_pengurangan`.`id_pegawai` = `tb_karyawan`.`id_pegawai`\n"
                    + "LEFT JOIN `tb_bagian` ON `tb_karyawan`.`kode_bagian` = `tb_bagian`.`kode_bagian`\n"
                    + "WHERE 1";
            rs = Utility.db.getStatement().executeQuery(sql);
            Object[] row = new Object[6];
            while (rs.next()) {
                row[0] = rs.getDate("tgl_lembur");
                row[1] = rs.getString("id_pegawai");
                row[2] = rs.getString("nama_pegawai");
                row[3] = rs.getString("nama_bagian");
                row[4] = rs.getInt("pengurangan");
                row[5] = rs.getString("keterangan");
                model.addRow(row);
            }
            ColumnsAutoSizer.sizeColumnsToFit(Table_data_pengurangan_lembur);
        } catch (Exception ex) {
            Logger.getLogger(JDialog_pengurangan_lembur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

