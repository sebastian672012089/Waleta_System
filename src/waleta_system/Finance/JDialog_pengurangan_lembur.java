    private void button_newActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_newActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            boolean check = true;
            if (Date_Lembur.getDate() == null
                    || txt_id_pegawai.getText().equals("") || txt_id_pegawai.getText() == null) {
                JOptionPane.showMessageDialog(this, "Silahkan lengkapi data diatas");
                check = false;
            }
            if (check) {
                String Query = "INSERT INTO `tb_lembur_pengurangan`(`tgl_lembur`, `id_pegawai`, `pengurangan`, `keterangan`) "
                        + "VALUES ('" + dateFormat.format(Date_Lembur.getDate()) + "','" + txt_id_pegawai.getText() + "'," + Spinner_pengurangan_lembur.getValue() + ",'" + txt_keterangan.getText() + "')";
                Utility.db.getConnection().createStatement();
                Utility.db.getStatement().executeUpdate(Query);
                refresh_tabel();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex + " : Nama dan tanggal sudah ada");
            Logger.getLogger(JDialog_Edit_NoRek_LevelGaji.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_newActionPerformed

    private void button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deleteActionPerformed
        // TODO add your handling code here:
        try {
            int j = Table_data_pengurangan_lembur.getSelectedRow();
            if (j == -1) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data yang ingin di hapus !");
            } else {
                int dialogResult = JOptionPane.showConfirmDialog(this, "Delete?", "Warning", 0);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    String Query = "DELETE FROM `tb_lembur_pengurangan` WHERE `tgl_lembur` = '" + Table_data_pengurangan_lembur.getValueAt(j, 0).toString() + "' "
                            + "AND `id_pegawai` = '" + Table_data_pengurangan_lembur.getValueAt(j, 1).toString() + "'";
                    Utility.db.getConnection().createStatement();
                    Utility.db.getStatement().executeUpdate(Query);
                    refresh_tabel();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(JDialog_Edit_NoRek_LevelGaji.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_deleteActionPerformed

